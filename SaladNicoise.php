                     <li><span>Cook the potatoes in a saucepan of boiling water for 10–15 minutes or until tender, adding the beans for the last 5 minutes of cooking. Drain and refresh under cold running water. Cut the potatoes in half, then transfer the potatoes and beans to a mixing bowl. Add the pepper and fennel.</span></li>
                    <li><span>To make the anchovy dressing, put 3 of the anchovy fillets and the peeled garlic in a food processor and process to a purée. Add the mustard, lemon juice and oil, and process until smooth. Alternatively, use a pestle and mortar, pounding the anchovies and garlic to a paste before whisking in the remaining ingredients. Pour the dressing over the vegetables and toss to coat.</span></li>
                    <li><span>Brush the tuna steaks with the oil and season with pepper. Heat a non-stick frying pan or ridged cast-iron grill pan. Add the tuna steaks and cook over a moderately high heat for about 4 minutes on each side or until lightly browned. Do not overcook or the tuna will be dry.</span></li>
